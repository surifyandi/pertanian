		<p>Farmer Training merupakan salah satu bentuk kegiatan yang diadakan oleh Petugas Pelayanan Veteriner Unggas Komersial (PVUK). Kegiatan ini diantaranya bertujuan untuk meningkatkan pengetahuan dan manajemen beternak sehingga produktivitas peternak menjadi lebih baik. Kegiatan ini telah berhasil diselenggarakan oleh Tim PVUK Kota Pontianak pada Jumâ€™at, 13 November 2015 yang lalu dan merupakan farmer training ke 2 yang telah dilakukan di Kota Pontianak.</p>
		<p>Kegiatan Farmer Training dilaksanakan di rumah Bapak Samudi, salah seorang peternak ayam broiler di Kelurahan Batu Layang, Kecamatan Pontianak Utara, Kota Pontianak. Kegiatan dihadiri Petugas PVUK Kota Pontianak, Endang Sayekti, S.Pt, MA, MSE dan drh. Sofia Febriyanita, petugas PVUK dari Dinas Peternakan dan Kesehatan Hewan Provinsi Kalimantan Barat, drh. Ahmad Mike A, Alfred Kompudu dari FAO dan drh. Imas Yuyun dari Dirjen Peternakan, serta peternak ayam broiler di kelurahan batu layang.</p>
		<p>Materi farmer training kali ini adalah brooding, yang disampaikan oleh drh. Ahmad Mike A. Pada ayam broiler (pedaging), masa brooding ialah periode pemeliharaan dari DOC masuk kandang (chick in) hingga umur 14 hari atau hingga pemanas tidak digunakan. Pemeliharaan di masa brooding menentukan baik tidaknya performa ayam di masa selanjutnya. Kesalahan manajemen pada periode ini seringkali tidak bisa dipulihkan (irreversible) dan berdampak negatif terhadap performa ayam di periode pemeliharaan berikutnya, sehingga periode ini perlu mendapat perhatian khusus dari Peternak. Oleh drh. Ahmad Mike dijelaskan pentingnya periode brooding, yang diringkas dengan P2T2HL, yakni Pemanas, Penyekat, Tirai, Termometer, Hygrometer dan Lampu. Keenam hal tersebut merupakan instrument penting yang wajib ada di kandang dan sangat berpengaruh pada periode brooding.</p>
		<p>Hal penting yang dapat menjadi indikasi bahwa periode brooding berhasil adalah ketika umur ayam 7 hari berat badan ayam minimal 4x berat pertama masuk kandang (chick in).</p>
		<p>Para peternak sangat antusias dengan kegiatan ini dan berharap kegiatan farmer training/pelatihan peternak ini dapat dilakukan kembali pada kesempatan yang lain sehingga pengetahuan mereka dalam manajemen pengelolaan ayam broiler dapat bertambah.</p>
		<p>(seksi Peternakan DPPK Kota Pontianak)</p>

